from dataclasses import dataclass
from typing import Any
import json


@dataclass
class ChartRecommendation:
    chart_type: str
    reason: str
    priority: int
    echarts_series_type: str


@dataclass
class RenderResult:
    chart_type: str
    html_path: str
    title: str
    record_count: int
    recommendation: ChartRecommendation


